const RolePromptsSection = () => {
  const [activeRole, setActiveRole] = useState(roles[0].id);
  const [expandedPrompt, setExpandedPrompt] = useState<string | null>(null);

  const currentRole = roles.find((r) => r.id === activeRole);

  return (
    <section id="roles" className="py-24 px-4">
      <div className="max-w-7xl mx-auto">
        <div className="mb-16">
          <span className="tag-red mb-4 block w-fit">Role-Based Prompts</span>
          <h2 className="kinetic-subtitle text-4xl md:text-6xl mb-4">
            <span className="kinetic-accent">역할별</span>{" "}
            <span className="text-stroke-red">프롬프트</span>
          </h2>
          <p className="text-muted-foreground max-w-2xl">
            AI 강사, 컨설턴트, 교사, 공무원, 학생 등 다양한 역할에 맞춤화된 프롬프트를 제공합니다.
          </p>
        </div>

        {/* Role Tabs */}
        <div className="flex flex-wrap gap-2 mb-8">
          {roles.map((role) => (
            <button
              key={role.id}
              onClick={() => setActiveRole(role.id)}
              className={`px-6 py-3 font-bold uppercase tracking-wider transition-all ${
                activeRole === role.id
                  ? "bg-primary text-primary-foreground"
                  : "bg-muted text-muted-foreground hover:bg-muted/80"
              }`}
            >
              <span className="mr-2">{role.icon}</span>
              {role.name}
            </button>
          ))}
        </div>

        {/* Category Tabs */}
        {currentRole && (
          <Tabs defaultValue="slides" className="w-full">
            <TabsList className="bg-muted/50 p-1 mb-8">
              {Object.entries(categoryLabels).map(([key, { label, icon }]) => (
                <TabsTrigger
                  key={key}
                  value={key}
                  className="data-[state=active]:bg-secondary data-[state=active]:text-secondary-foreground px-6 py-2"
                >
                  <span className="mr-2">{icon}</span>
                  {label}
                </TabsTrigger>
              ))}
            </TabsList>

            {Object.entries(currentRole.categories).map(([category, prompts]) => (
              <TabsContent key={category} value={category}>
                <div className="grid gap-4">
                  {prompts.map((item, index) => (
                    <div
                      key={index}
                      className="card-kinetic animate-slide-up"
                      style={{ animationDelay: `${index * 0.05}s` }}
                    >
                      <div
                        className="flex items-center justify-between cursor-pointer"
                        onClick={() =>
                          setExpandedPrompt(
                            expandedPrompt === `${category}-${index}`
                              ? null
                              : `${category}-${index}`
                          )
                        }
                      >
                        <div className="flex items-center gap-4">
                          <span className="text-2xl font-display text-primary">
                            {String(index + 1).padStart(2, "0")}
                          </span>
                          <h3 className="text-lg font-bold">{item.title}</h3>
                        </div>
                        <div
                          className={`text-xl transition-transform duration-300 ${
                            expandedPrompt === `${category}-${index}` ? "rotate-45" : ""
                          }`}
                        >
                          +
                        </div>
                      </div>

                      <div
                        className={`overflow-hidden transition-all duration-500 ${
                          expandedPrompt === `${category}-${index}`
                            ? "max-h-[500px] opacity-100 mt-4"
                            : "max-h-0 opacity-0"
                        }`}
                      >
                        <div className="prompt-box">{item.prompt}</div>
                      </div>
                    </div>
                  ))}
                </div>
              </TabsContent>
            ))}
          </Tabs>
        )}
      </div>
    </section>
  );
};

export default RolePromptsSection;
